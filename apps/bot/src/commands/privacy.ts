import type { Context } from "grammy";

export async function sendPrivacy(ctx: Context): Promise<void> {
  await ctx.reply(
    [
      "Твои главы приватны по умолчанию.",
      "",
      "Ты сам решаешь, что сохранить, удалить или отправить другим. В логах не хранится raw-текст записей, а публичные страницы открываются только по длинному приватному токену.",
      "",
      "Можно запросить экспорт данных или удалить последнюю главу командами /export и /delete_last."
    ].join("\n")
  );
}

